public class Code03_09 {
    public static void main(String [] args){
        int b = 20;
        b++ ; System.out.print(b+" ");
        b-- ; System.out.print(b+" ");
        b += 3 ; System.out.print(b+ " ");
        b -= 3 ; System.out.print(b+ " ");
        b *= 3 ; System.out.print(b+ " ");
        b /= 3 ; System.out.print(b+ " ");
        b %= 3 ; System.out.print(b+ " ");
    }
}
